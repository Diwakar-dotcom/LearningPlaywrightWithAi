let age = "23"; // String Literal
let isStudent = true; // Boolean Literal
let height = 5.9; // Number Literal
let name = "Diwakar"; // String Literal
let address = null; // Null Literal
let hobbies = ["reading", "coding", "gaming"]; // Array Literal
let person = { name: "Diwakar", age: 23 }; // Object Literal
let undefinedVar; // Undefined Literal: The variable is declared but not assigned a value, so it is undefined by default.

// Numeric Literals - Nummeric
// 1. int
// 2. float

// typeof - operator - It will tell you what is a data type variable has. 

console.log(typeof age); // string
console.log(typeof isStudent); // boolean
console.log(typeof height); // number
console.log(typeof name); // string
console.log(typeof address); // object
console.log(typeof hobbies); // object
console.log(typeof person); // object
console.log(typeof undefinedVar); // undefined
