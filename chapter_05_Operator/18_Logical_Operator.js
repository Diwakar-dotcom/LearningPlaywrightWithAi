// Logical Operator

// &&, ||, ! - These are three logical operators we have in js

// && - AND gate
let a = true;
let b = false;

console.log("a = ", a);
console.log("b = ", b);

console.log("a&&b = ",a&&b);

// || - OR gate
console.log("a||b = ",a||b);

// ! - NOT gate

console.log("!a = ", !a);

