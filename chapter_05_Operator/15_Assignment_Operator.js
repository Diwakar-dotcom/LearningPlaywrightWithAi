// Assignment Operator
// The assignment operator (=) is used to assign a value to a variable.

let x = 10; // Assigning the value 10 to the variable x
let y = 5;  // Assigning the value 5 to the variable y

console.log(x); // Output: 10
console.log(y); // Output: 5

// You can also use the assignment operator with expressions
let z = x + y; // Assigning the result of x + y to z
console.log(z); // Output: 15

// The assignment operator can also be combined with other operators
x += 5; // Equivalent to x = x + 5
console.log(x); // Output: 15

y *= 2; // Equivalent to y = y * 2
console.log(y); // Output: 10  

