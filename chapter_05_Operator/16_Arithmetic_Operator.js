// Arithmetic Operators
// Addition (+)
let a = 10;
let b = 5;
let sum = a + b; // 15
console.log("Sum:", sum);

// Subtraction (-)
let difference = a - b; // 5
console.log("Difference:", difference);

// Multiplication (*)
let product = a * b; // 50
console.log("Product:", product);

// Division (/) --> This will give the quotient of the division
let quotient = a / b; // 2
console.log("Quotient:", quotient);

// Modulus (%) --> This will give the remainder of the division
let remainder = a % b; // 0
console.log("Remainder:", remainder);

// Exponentiation (**)
let power = a ** b; // 100000
console.log("Power:", power);
