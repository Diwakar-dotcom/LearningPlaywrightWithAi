// For Loop
// Help you write repeat a block of code

// ICU
// Initialization - let i = 10 or i = 0;
// Condition - i < 50 or i > 50 i <= 10 i >= 10;
// Updation - i++ or --i;

// post-increment updation example
for (let i = 1; i <= 10; i++) {
    console.log(i);
}

// pre-increment updatation example
for (let i = 1; i <= 10; ++i){
    console.log(i)
}

// In above both examples outcome will be same because we are using the updation value in next step 