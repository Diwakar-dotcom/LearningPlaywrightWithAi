// Question: How many times each loop executes and prints value ?

for (let diwakar = 0; diwakar < 10; diwakar++) {
    console.log(diwakar); // 10 times (0 to 9)
}

for (let diwakar = 0; diwakar <= 10; diwakar++) {
    console.log(diwakar); // 11 times (0 to 10)
}

for (let diwakar = 1; diwakar <= 10; diwakar++) {
    console.log(diwakar); // 10 times (1 to 10)
}